/* USER CODE BEGIN 4 */
void RPM_R_func(void) {
	FSM_FLAG = 0;
	rpm_right_buffer[RPMCounter_R] = calculate_rpm(RIGHT_ID);
	RPMCounter_R++;

	if (RPMCounter_R == 4) {

		TxHeader.StdId = ENGINE_ECU_ROT_R;
		encode_vec_16_to_8(rpm_right_buffer, TxData);
		HAL_CAN_AddTxMessage(&hcan, &TxHeader, TxData, &TxMailbox);
		RPMCounter_R = 0;
	}

	state_FMS = RPM_L_STATE;
}

void RPM_L_func(void) {
	FSM_FLAG = 0;
	rpm_left_buffer[RPMCounter_L] = calculate_rpm(LEFT_ID);
	RPMCounter_L++;

	if (RPMCounter_L == 4) {
		TxHeader.StdId = ENGINE_ECU_ROT_L;
		encode_vec_16_to_8(rpm_left_buffer, TxData);
		HAL_CAN_AddTxMessage(&hcan, &TxHeader, TxData, &TxMailbox);
		RPMCounter_L = 0;
	}

	state_FMS = TEMP_R_STATE;
}

void Temp_R_func(void) {
	FSM_FLAG = 0;
	temp_right_buffer[TempCounter_R] = get_temperature(RIGHT_ID);
	TempCounter_R++;

	if (TempCounter_R == 4) {
		TxHeader.StdId = ENGINE_ECU_TEMP_R;
		encode_vec_16_to_8(temp_right_buffer, TxData);
		HAL_CAN_AddTxMessage(&hcan, &TxHeader, TxData, &TxMailbox);
		TempCounter_R = 0;
		HAL_GPIO_TogglePin(LED_PORT, LED_PIN);
	}

	state_FMS = TEMP_L_STATE;
}

void Temp_L_func(void) {
	FSM_FLAG = 0;
	temp_left_buffer[TempCounter_L] = get_temperature(LEFT_ID);
	TempCounter_L++;

	if (TempCounter_L == 4) {
		TxHeader.DLC = 8; // length of can packet
		TxHeader.RTR = CAN_RTR_DATA;

		TxHeader.StdId = ENGINE_ECU_TEMP_L;
		encode_vec_16_to_8(temp_left_buffer, TxData);
		HAL_CAN_AddTxMessage(&hcan, &TxHeader, TxData, &TxMailbox);

		TempCounter_L = 0;
	}

	state_FMS = RPM_R_STATE;
}



void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {

	if(htim->Instance == RPM_COUNTER_TIMER_LEFT){
		rpm_get_counter(LEFT_ID);
		return;
	}
	if(htim->Instance == RPM_COUNTER_TIMER_RIGHT){
		rpm_get_counter(RIGHT_ID);
		return;
	}
	if (htim->Instance == ADC_MOVING_AVERAGE_TIMER) {

		FSM_FLAG = 1;

	}

}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
	if (GPIO_Pin == EXTI_PIN_LEFT) {
		update_time(LEFT_ID);
	}
	if(GPIO_Pin == EXTI_PIN_RIGHT){
		update_time(RIGHT_ID);

	}

}
